_BROWSER_TRACE_SCRIPT = r"""
(() => {
  if (window.__testEvaluatorTrace) return;
  const stubs = new Set(__TEST_EVALUATOR_STUBS__);
  const trace = {storage: [], network: [], browser_api: []};
  Object.defineProperty(window, "__testEvaluatorTrace", {value: trace, configurable: true});
  const clean = (value) => {
    if (value === undefined) return null;
    if (value === null || ["string", "number", "boolean"].includes(typeof value)) return value;
    try { return JSON.parse(JSON.stringify(value)); } catch (_) { return String(value); }
  };
  const push = (kind, operation, detail = {}) => {
    trace[kind].push({timestamp: Date.now(), operation, ...clean(detail)});
  };

  for (const method of ["setItem", "getItem", "removeItem", "clear"]) {
    const original = Storage.prototype[method];
    if (!original) continue;
    Storage.prototype[method] = function(...args) {
      const area = this === window.localStorage ? "localStorage" : "sessionStorage";
      push("storage", method, {area, key: clean(args[0]), value: clean(args[1])});
      return original.apply(this, args);
    };
  }

  if (window.fetch) {
    const originalFetch = window.fetch.bind(window);
    window.fetch = function(input, init = {}) {
      const url = typeof input === "string" ? input : input?.url;
      push("network", "fetch", {url, method: init.method || "GET"});
      if (stubs.has("network") && /^https?:/i.test(url || "")) {
        push("network", "fetch_stubbed", {url});
        return Promise.resolve(new Response("{}", {status: 503, headers: {"Content-Type": "application/json"}}));
      }
      return originalFetch(input, init);
    };
  }

  if (window.XMLHttpRequest) {
    const originalOpen = XMLHttpRequest.prototype.open;
    const originalSend = XMLHttpRequest.prototype.send;
    XMLHttpRequest.prototype.open = function(method, url, ...rest) {
      this.__testEvaluatorRequest = {method, url};
      return originalOpen.call(this, method, url, ...rest);
    };
    XMLHttpRequest.prototype.send = function(body) {
      push("network", "xhr", {...(this.__testEvaluatorRequest || {}), body: clean(body)});
      if (stubs.has("network") && /^https?:/i.test(this.__testEvaluatorRequest?.url || "")) {
        push("network", "xhr_stub_requested", this.__testEvaluatorRequest || {});
      }
      return originalSend.call(this, body);
    };
  }

  const speech = window.speechSynthesis;
  if (speech?.speak) {
    const originalSpeak = speech.speak.bind(speech);
    speech.speak = function(utterance) {
      push("browser_api", "speechSynthesis.speak", {text: utterance?.text || ""});
      if (!stubs.has("speech")) return originalSpeak(utterance);
    };
  }
  const clipboard = navigator.clipboard;
  if (clipboard?.writeText) {
    const originalWrite = clipboard.writeText.bind(clipboard);
    clipboard.writeText = function(text) {
      push("browser_api", "clipboard.writeText", {text});
      return stubs.has("clipboard") ? Promise.resolve() : originalWrite(text);
    };
  }
  if (window.Notification?.requestPermission) {
    const originalPermission = Notification.requestPermission.bind(Notification);
    Notification.requestPermission = function(...args) {
      push("browser_api", "Notification.requestPermission", {});
      return stubs.has("notification") ? Promise.resolve("denied") : originalPermission(...args);
    };
  }
})();
"""


def _write_coverage(driver):
    method = os.environ.get("TEST_EVALUATOR_COVERAGE_METHOD", "none")
    if method == "istanbul":
        if getattr(driver, "_test_evaluator_coverage_written", False):
            return
        try:
            result = driver.execute_script("return window.__coverage__ || {};")
            artifacts = Path(os.environ["TEST_EVALUATOR_ARTIFACTS"])
            artifacts.mkdir(parents=True, exist_ok=True)
            (artifacts / "istanbul_coverage.json").write_text(
                json.dumps(result, indent=2), encoding="utf-8"
            )
            driver._test_evaluator_coverage_written = True
        except Exception:
            pass
        return
    if not getattr(driver, "_test_evaluator_coverage_started", False):
        return
    if getattr(driver, "_test_evaluator_coverage_written", False):
        return
    try:
        result = driver.execute_cdp_cmd("Profiler.takePreciseCoverage", {})
        artifacts = Path(os.environ["TEST_EVALUATOR_ARTIFACTS"])
        artifacts.mkdir(parents=True, exist_ok=True)
        (artifacts / "coverage.json").write_text(json.dumps(result, indent=2), encoding="utf-8")
        driver._test_evaluator_coverage_written = True
    except Exception:
        pass
    finally:
        try:
            driver.execute_cdp_cmd("Profiler.stopPreciseCoverage", {})
            driver.execute_cdp_cmd("Profiler.disable", {})
        except Exception:
            pass


def _write_observability(driver):
    if getattr(driver, "_test_evaluator_observability_written", False):
        return
    artifacts = Path(os.environ["TEST_EVALUATOR_ARTIFACTS"])
    artifacts.mkdir(parents=True, exist_ok=True)
    try:
        result = driver.execute_script("return window.__testEvaluatorTrace || null;")
        if result is not None:
            (artifacts / "browser_trace.json").write_text(
                json.dumps(result, indent=2), encoding="utf-8"
            )
    except Exception:
        pass
    try:
        raw_logs = driver.get_log("performance")
        network_logs = []
        for item in raw_logs:
            try:
                message = json.loads(item.get("message", "{}"))
                event = message.get("message", {})
                if str(event.get("method", "")).startswith("Network."):
                    network_logs.append(event)
            except Exception:
                continue
        if network_logs:
            (artifacts / "network_log.json").write_text(
                json.dumps(network_logs, indent=2), encoding="utf-8"
            )
    except Exception:
        pass
    driver._test_evaluator_observability_written = True


def before_all(context):
    from selenium import webdriver
    from selenium.webdriver.chrome.service import Service

    original_chrome = webdriver.Chrome

    def managed_chrome(*args, **kwargs):
        # E2EDev tests normally call webdriver.Chrome() without options. Preserve
        # explicit caller arguments, but make the common path deterministic on
        # CI/Linux by selecting the installed browser and matching driver.
        if not args:
            options = kwargs.get("options")
            if options is None:
                options = webdriver.ChromeOptions()
                kwargs["options"] = options
            if not options.binary_location:
                chrome_binary = shutil.which("google-chrome") or shutil.which("chromium")
                if chrome_binary:
                    options.binary_location = chrome_binary
            if os.environ.get("TEST_EVALUATOR_HEADLESS", "1") == "1":
                for argument in (
                    "--headless=new",
                    "--no-sandbox",
                    "--disable-dev-shm-usage",
                    "--window-size=1920,1080",
                    "--allow-file-access-from-files",
                ):
                    options.add_argument(argument)
            if os.environ.get("TEST_EVALUATOR_OBSERVABILITY", "1") == "1":
                options.set_capability(
                    "goog:loggingPrefs", {"browser": "ALL", "performance": "ALL"}
                )
            if kwargs.get("service") is None:
                driver_binary = shutil.which("chromedriver")
                if driver_binary:
                    kwargs["service"] = Service(driver_binary)
        driver = original_chrome(*args, **kwargs)
        if os.environ.get("TEST_EVALUATOR_OBSERVABILITY", "1") == "1":
            try:
                stubs = [
                    item.strip()
                    for item in os.environ.get("TEST_EVALUATOR_BROWSER_STUBS", "").split(",")
                    if item.strip()
                ]
                source = _BROWSER_TRACE_SCRIPT.replace(
                    "__TEST_EVALUATOR_STUBS__", json.dumps(stubs)
                )
                driver.execute_cdp_cmd(
                    "Page.addScriptToEvaluateOnNewDocument", {"source": source}
                )
            except Exception:
                pass
        if os.environ.get("TEST_EVALUATOR_COVERAGE_METHOD") == "cdp_precise_coverage":
            try:
                driver.execute_cdp_cmd("Profiler.enable", {})
                driver.execute_cdp_cmd(
                    "Profiler.startPreciseCoverage",
                    {"callCount": True, "detailed": True, "allowTriggeredUpdates": False},
                )
                driver._test_evaluator_coverage_started = True
            except Exception:
                pass
        original_quit = driver.quit

        def quit_with_artifacts():
            _write_coverage(driver)
            _write_observability(driver)
            return original_quit()

        driver.quit = quit_with_artifacts
        return driver

    webdriver.Chrome = managed_chrome


