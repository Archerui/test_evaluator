def _safe_segment(value: str) -> str:
    cleaned = re.sub(r"[^A-Za-z0-9_.-]+", "_", value).strip("._")
    return cleaned[:64] or "test"


def _workspace_name(record_key: str) -> str:
    digest = sha256(record_key.encode("utf-8")).hexdigest()[:10]
    return f"{_safe_segment(record_key)}-{digest}"


def _entry_path(inventory_source: Path, app_root: Path, entry_html: str | None) -> Path:
    if not entry_html:
        raise MaterializationError("No HTML entry point was discovered for this project")
    relative = Path(entry_html)
    if relative.is_absolute() or ".." in relative.parts:
        raise MaterializationError(f"Unsafe entry HTML path: {entry_html}")
    source_entry = (inventory_source / relative).resolve()
    try:
        source_entry.relative_to(inventory_source.resolve())
    except ValueError as error:
        raise MaterializationError(f"Entry HTML escapes source root: {entry_html}") from error
    if not source_entry.is_file():
        raise MaterializationError(f"Entry HTML does not exist: {source_entry}")
    return (app_root / relative).resolve()


def _rewrite_step_paths(step_code: str, entry_html: Path) -> tuple[str, list[dict[str, str]]]:
    rewritten = step_code
    changes: list[dict[str, str]] = []
    entry_uri = entry_html.as_uri()

    def replace_url(match: re.Match[str]) -> str:
        original = match.group(0)
        if original != entry_uri:
            changes.append({"kind": "file_url", "original": original, "replacement": entry_uri})
        return entry_uri

    rewritten = _FILE_URL.sub(replace_url, rewritten)

    def replace_assignment(match: re.Match[str]) -> str:
        original_value = match.group("value")
        replacement = str(entry_html)
        if original_value != replacement:
            changes.append(
                {
                    "kind": "file_path_assignment",
                    "original": original_value,
                    "replacement": replacement,
                }
            )
        quote = match.group("quote")
        return f"{match.group('prefix')}{quote}{replacement}{quote}"

    rewritten = _FILE_PATH_ASSIGNMENT.sub(replace_assignment, rewritten)
    return rewritten, changes


def materialize_test(request: TestMaterializerInput) -> TestMaterializerOutput:
    """Copy application source and write one feature/steps pair without touching the clone."""

    inventory = request.inventory
    record = request.record
    if inventory.project_id != record.project_id:
        raise MaterializationError(
            f"Inventory project {inventory.project_id!r} does not match record project {record.project_id!r}"
        )
    if not record.scenario.strip():
        raise MaterializationError("The test has no Gherkin scenario")
    if not record.step_code.strip():
        raise MaterializationError("The test has no Behave step implementation")
    if not inventory.source_root:
        raise MaterializationError("The project has no discovered source root")

    source_root = Path(inventory.source_root).resolve()
    if not source_root.is_dir():
        raise MaterializationError(f"Source root does not exist: {source_root}")

    workspace_root = Path(request.output_dir).resolve() / _workspace_name(record.record_key)
    app_root = workspace_root / "app"
    features_root = workspace_root / "features"
    steps_root = features_root / "steps"
    artifacts_dir = workspace_root / "artifacts"
    if workspace_root.exists():
        shutil.rmtree(workspace_root)
    workspace_root.mkdir(parents=True)
    shutil.copytree(
        source_root,
        app_root,
        ignore=shutil.ignore_patterns(".DS_Store", "__pycache__", "*.pyc"),
    )
    steps_root.mkdir(parents=True)
    artifacts_dir.mkdir(parents=True)

    entry_html = _entry_path(source_root, app_root, inventory.entry_html)
    feature_file = features_root / "test.feature"
    steps_file = steps_root / "steps.py"
    environment_file = features_root / "environment.py"
    rewritten_code, rewritten_paths = _rewrite_step_paths(record.step_code, entry_html)

    feature_file.write_text(record.scenario.rstrip() + "\n", encoding="utf-8")
    steps_file.write_text(rewritten_code.rstrip() + "\n", encoding="utf-8")
    environment_file.write_text(_ENVIRONMENT_PY, encoding="utf-8")

    workspace = WorkspaceSpec(
        record_key=record.record_key,
        workspace_root=str(workspace_root),
        app_root=str(app_root),
        feature_file=str(feature_file),
        steps_file=str(steps_file),
        entry_html=str(entry_html),
        artifacts_dir=str(artifacts_dir),
        rewritten_paths=rewritten_paths,
    )
    return TestMaterializerOutput(
        agent="test_materializer",
        run_id=request.run_id,
        mode="full",
        project_id=record.project_id,
        suite_key=record.suite_key,
        record_key=record.record_key,
        status=Status.PASS,
        confidence=1.0,
        workspace=workspace,
        artifacts=[
            ArtifactRef(
                kind="workspace",
                path=str(workspace_root),
                description="Isolated Behave/Selenium workspace",
            )
        ],
    )
