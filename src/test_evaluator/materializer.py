class MaterializationError(RuntimeError):
    """Raised when one test cannot be converted into a runnable workspace."""


_FILE_URL = re.compile(r"file://[^\"'\s)]+", re.IGNORECASE)
_FILE_PATH_ASSIGNMENT = re.compile(
    r"(?m)^(?P<prefix>\s*file_path\s*=\s*)(?P<quote>['\"])(?P<value>[^'\"]+)(?P=quote)"
)


